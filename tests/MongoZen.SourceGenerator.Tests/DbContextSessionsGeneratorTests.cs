    public MongoZen.IMutableDbSet<TEntity> Query<TEntity>() where TEntity : class
    {
        if (typeof(TEntity) == typeof(Blog))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Blogs;
        }
        else if (typeof(TEntity) == typeof(Post))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Posts;
        }
        else
        {
            throw new System.ArgumentException($""Entity type {typeof(TEntity).Name} is not part of this DbContext."");
        }
    }

    [System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
    public void Add<TEntity>(TEntity entity) where TEntity : class
    {
        Store(entity);
    }

    [System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
    public void Attach<TEntity>(TEntity entity) where TEntity : class
    {
        if (entity is Blog e0)
        {
            Blogs.Attach(e0);
        }
        else if (entity is Post e1)
        {
            Posts.Attach(e1);
        }
        else
        {
            throw new System.ArgumentException($""Entity type {typeof(TEntity).Name} is not part of this DbContext."");
        }
    }

    [System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
    public void Remove<TEntity>(TEntity entity) where TEntity : class
    {
        Delete(entity);
    }

    [System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
    public void Remove<TEntity>(object id) where TEntity : class
    {
        Delete<TEntity>(id);
    }

    [System.ComponentModel.EditorBrowsable(System.ComponentModel.EditorBrowsableState.Never)]
    public void Remove<TEntity>(global::MongoZen.DocId id) where TEntity : class
    {
        Delete<TEntity>(id);
    }

    public async ValueTask<TEntity?> LoadAsync<TEntity>(object id, System.Threading.CancellationToken cancellationToken = default) where TEntity : class
    {
        if (typeof(TEntity) == typeof(Blog))
        {
            return (TEntity?)(object?)await Blogs.LoadAsync(id, cancellationToken);
        }
        else if (typeof(TEntity) == typeof(Post))
        {
            return (TEntity?)(object?)await Posts.LoadAsync(id, cancellationToken);
        }
        else
        {
            throw new System.ArgumentException($""Entity type {typeof(TEntity).Name} is not part of this DbContext."");
        }
    }

    public MongoZen.IMutableDbSet<TEntity> Include<TEntity>(System.Linq.Expressions.Expression<System.Func<TEntity, object?>> path) where TEntity : class
    {
        if (typeof(TEntity) == typeof(Blog))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Blogs.Include((System.Linq.Expressions.Expression<System.Func<Blog, object?>>)(object)path);
        }
        else if (typeof(TEntity) == typeof(Post))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Posts.Include((System.Linq.Expressions.Expression<System.Func<Post, object?>>)(object)path);
        }
        else
        {
            throw new System.ArgumentException($""Entity type {typeof(TEntity).Name} is not part of this DbContext."");
        }
    }

    public MongoZen.IMutableDbSet<TEntity> Include<TEntity, TInclude>(System.Linq.Expressions.Expression<System.Func<TEntity, object?>> path) where TEntity : class where TInclude : class
    {
        if (typeof(TEntity) == typeof(Blog))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Blogs.Include<TInclude>((System.Linq.Expressions.Expression<System.Func<Blog, object?>>)(object)path);
        }
        else if (typeof(TEntity) == typeof(Post))
        {
            return (MongoZen.IMutableDbSet<TEntity>)(object)Posts.Include<TInclude>((System.Linq.Expressions.Expression<System.Func<Post, object?>>)(object)path);
        }
        else
        {
            throw new System.ArgumentException($""Entity type {typeof(TEntity).Name} is not part of this DbContext."");
        }
    }
}
";

        var compilation = CreateCompilation(source);
        var generator = new DbContextSessionsGenerator();
        var driver = CSharpGeneratorDriver.Create(generator);
        driver = (CSharpGeneratorDriver)driver.RunGeneratorsAndUpdateCompilation(
            compilation, out var outputCompilation, out var diagnostics);

        var result = driver.GetRunResult();
        Assert.Single(result.Results);

        var generatorResult = result.Results[0];
        Assert.Empty(generatorResult.Diagnostics);
        Assert.Single(generatorResult.GeneratedSources);

        var generatedSource = generatorResult.GeneratedSources[0];
        Assert.Equal("BloggingContextSession.g.cs", generatedSource.HintName);

        var actualText = generatedSource.SourceText.ToString();
        Assert.Equal(Normalize(expected), Normalize(actualText));
    }

